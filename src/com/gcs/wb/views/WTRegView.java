    private boolean validateLicensePlate(String lfart, String traid) {
        boolean result = false;
        if (lfart != null && traid != null && (lfart.equalsIgnoreCase("ZTLF") || lfart.equalsIgnoreCase("NL"))) {
            String txt = traid;
            String doLicPlate;
            int idx1 = txt.indexOf("|");
            int idx2 = txt.indexOf("\\");
            if (idx1 >= 0 || idx2 >= 0) {
                int splitPos = idx1 < 0 ? idx2 : idx1;
                int bIdx = splitPos - 8;
                if (bIdx < 0) {
                    bIdx = 0;
                }
                doLicPlate = txt.substring(bIdx, splitPos);
                splitPos++;
            } else {
                int countSpace = 0;
                int[] pos = new int[3];
                char[] chars = txt.toCharArray();
                for (int x = 0; x < chars.length; x++) {
                    char c = chars[x];
                    if (c == ' ') {
                        pos[countSpace] = x;
                        countSpace++;
                    }
                }
                if (countSpace == 3) {
                    int splitPos = pos[1];
                    int bIdx = splitPos - 8;
                    if (bIdx < 0) {
                        bIdx = 0;
                    }
                    doLicPlate = txt.substring(bIdx, splitPos);
                    splitPos++;
                } else {
                    doLicPlate = traid;
                }
            }
            if (doLicPlate.equalsIgnoreCase(newWeightTicket.getPlateNo())) {
                result = true;
            } else {
                result = false;
                JOptionPane.showMessageDialog(rootPane, "Số xe \"" + newWeightTicket.getPlateNo()
                        + "\" không khớp với số được đăng ký trong D.O: " + doLicPlate);
            }
        } else if (newWeightTicket != null) {
            Vehicle vehicle = vehicleRepository.findByPlateNo(txtNPlateNo.getText().trim());
            if (vehicle == null) {
                result = false;
                JOptionPane.showMessageDialog(rootPane, "Số xe \" " + txtNPlateNo.getText().trim()
                        + " \" chưa được đăng ký để xuất/nhập hàng!");
            } else {
                result = true;
                if (vehicle.isProhibit()) {
                    result = false;
                    JOptionPane.showMessageDialog(rootPane, "Số xe \" " + txtNPlateNo.getText().trim()
                            + " \" đang bị cấm xuất/nhập hàng!");
                }

                List<TransportAgentVehicle> transportAgentVehicles = transportAgentVehicleRepository.findByVehicleId(vehicle.getId());
                if (transportAgentVehicles != null && transportAgentVehicles.size() == 1) {
                    abbr = transportAgentVehicles.get(0).getTransportAgent().getAbbr();
                }
            }
        }
        return result;
    }

